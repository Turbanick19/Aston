package org.Lessone_12_testiNG;

public class CompareNumbers {

    public static boolean areEqual(int a, int b) {
        return a == b;
    }

    public static boolean isGreaterThan(int a, int b) {
        return a > b;
    }

    public static boolean isLessThan(int a, int b) {
        return a < b;
    }
}
